public class Clase02_Variables {

	/**
	 * Método principal que muestra cómo declarar e inicializar una variable de tipo byte.
	 * 
	 * @param args Los argumentos de línea de comandos (no utilizados en este ejemplo).
	 */
    public static void main(String[] args) {
        
        // Declaración e inicialización de la variable en una sola línea
        byte edad = 35;
        
        // Mostrar la edad en consola
        System.out.println("La edad es: " + edad);

    }

}
